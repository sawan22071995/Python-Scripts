# program to demonstrate FOR loop

fruits = ["sawan", "watermelon", "mangoes", "grapes"]
for item in fruits:
    print(item)

# range is used to generate sequence of number
#we can also specify start,stop and step-size in range function
# range(start, end, step-size by default 1)

# program to print number divisible by 2 till 50
for i in range(2,51,2):
    print(i)